module.exports = {
	name: 't',
	description: 'Erstellt ein Bild eines Template-Codes',
	async execute(client, message, args) {
		const code = args.shift();
		const name = args.join(" ");
		const key = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/';

		let stream = "";
		[...code].forEach(c => {
			stream += key.indexOf(c).toString(2).padStart(6, '0').split("").reverse().join("");
		});
		stream = stream.padEnd(572, "0").split("");

		const template = createTemplate(stream);

		const primary = message.guild.emojis.cache.find(emoji => emoji.name === template.primary.symbol);
		const secondary = message.guild.emojis.cache.find(emoji => emoji.name === template.secondary.symbol);

		let image = await createImage(template.skills.map(a => a.id), );

		const attachment = new Discord.MessageAttachment(image, 'template.png');

		const response = new Discord.MessageEmbed()
			.setColor(template.primary.color)
			.setDescription(primary.toString() + ' ' + secondary.toString() + ' ' + name)
			.setFooter(code)
			.attachFiles([attachment])
			.setImage('attachment://template.png');

		message.channel.send(response)
			.then(async response => {
				await response.react('✏️');
				await response.react('🗑️');
				//response.id
				client.templateMessages.set(response.id, message.author.id);
			});
	}
};

function createTemplate (stream) {
	let encoded = {};

	encoded.type 			= parseInt(stream.splice(0, 4).reverse().join(""), 2);
	encoded.version 		= parseInt(stream.splice(0, 4).reverse().join(""), 2);
	encoded.bitsClass 		= parseInt(stream.splice(0, 2).reverse().join(""), 2) * 2 + 4;
	encoded.primary 		= parseInt(stream.splice(0, encoded.bitsClass).reverse().join(""), 2);
	encoded.secondary 		= parseInt(stream.splice(0, encoded.bitsClass).reverse().join(""), 2);
	encoded.countAttributes = parseInt(stream.splice(0, 4).reverse().join(""), 2);
	encoded.bitsAttributes 	= parseInt(stream.splice(0, 4).reverse().join(""), 2) + 4;

	encoded.attributes = [];
	for (let i = 0; i < encoded.countAttributes; i++) {
		encoded.attributes.push({
			name :parseInt(stream.splice(0, encoded.bitsAttributes).reverse().join(""), 2),
			rank : parseInt(stream.splice(0, 4).reverse().join(""), 2)
		});
	}

	encoded.bitsSkills 	= parseInt(stream.splice(0, 4).reverse().join(""), 2) + 8;

	encoded.skills = [];
	for (let i = 0; i < 8; i++) {
		encoded.skills.push(parseInt(stream.splice(0, encoded.bitsSkills).reverse().join(""), 2));
	}

	let template = {};
	template.primary = classes[encoded.primary];
	template.secondary = classes[encoded.secondary];
	template.attributes = encoded.attributes.map(a => {
		return {
			name : attributes[a.name],
			rank : a.rank
		}
	});
	template.skills = encoded.skills.map(s => {
		return {
			
			id : s,
			name : skills[s]
		}
	});
	return template;
}

async function createImage(ids) {
	const size = 200;
	const canvas = Canvas.createCanvas(size * 8, size);
	const ctx = canvas.getContext('2d');
	ctx.fillStyle = "#FFFFFF";
	ctx.fillRect(0, 0, size * 8, size);

	let imageLoaders = ids.map((id) => {
		return Canvas.loadImage('./skillicons/' + id + '.jpg');
	});

	await Promise.all(imageLoaders)
		.then((images) => {
			for (let i = 0; i < images.length; i++) {
				ctx.drawImage(images[i], i * size, 0, size, size);
			}
		})
	
	return canvas.toBuffer();
}